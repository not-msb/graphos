; putKey(key_code: u8) void
putKey:
    xor edx, edx
    mov ecx, 0x0f

    cmp ebx, 0x01
    je .unhandled

    cmp ebx, 0x58
    jle .genericKey

    jmp .unhandled
.genericKey:
    mov dl, byte [keyCodeTable+ebx]
    xor ebx, ebx
    mov bl, dl
    call putCEntry
    ret
.unhandled:
    ret

